    public void setCursoSelecionado(String curso) {
        String cursosAtuais = campoCursos.getText();
        if (cursosAtuais.isEmpty()) {
            campoCursos.setText(curso);
        } else {
            campoCursos.setText(cursosAtuais + ", " + curso);
        }
    }

